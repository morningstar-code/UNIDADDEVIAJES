// Función para buscar ciudades por país
export function searchCitiesByCountry(country: string, query: string): string[] {
  if (!country || !query) return []
  const cities = citiesByCountry[country] || []
  const lowerQuery = query.toLowerCase().trim()
  return cities.filter((city) => city.toLowerCase().startsWith(lowerQuery))
}

// Obtener todas las ciudades de un país
export function getCitiesByCountry(country: string): string[] {
  return citiesByCountry[country] || []
}
